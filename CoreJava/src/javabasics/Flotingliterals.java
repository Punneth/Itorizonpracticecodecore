package javabasics;

public class Flotingliterals {

	public static void main(String[] args) {
		System.out.println(74589787.12);//Standard Notation
		System.out.println(7.458978712E7);//Standard Notation.
		System.out.println(0.314567821);//Scientific Notation.
		System.out.println(3.14567821E-1);//Scientific Notation.
	}

}
